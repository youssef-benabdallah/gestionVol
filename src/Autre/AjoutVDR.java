    void chargerTout() throws UnknownHostException
    {
    Connexion connexion = new Connexion("Airport");
    if(listV.getSelectedIndex()>=0)
    {
    DBCursor list = connexion.findListe("Vol",new BasicDBObject().append("num",listV.getSelectedItem().toString()));
    DBObject object = list.next();
    distance.setText(object.get("distance").toString()+" Km");
    frequence.setText(object.get("frequence").toString()+" Hz");
    }
    if(listD.getSelectedIndex()>=0)
    {
    DBCursor list = connexion.findListe("Depart",new BasicDBObject().append("num",listD.getSelectedItem().toString()));
    DBObject object = list.next();
    jour.setText(object.get("jour").toString()+"/"+object.get("mois").toString()+"/"+object.get("annee").toString());
    }
    }
    
