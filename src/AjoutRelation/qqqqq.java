    private void ajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterActionPerformed

        if(qc.getText().length()==0)
        {
            JOptionPane.showMessageDialog(null, "*****  Saisir Le Quantite De Carburant  *****");

        } else
        {
            try {
                //boolean r=true;
                Avion A = new Avion(listA.getSelectedItem().toString());
                Depart D = new Depart(listD.getSelectedItem().toString());

                DAR dar = new DAR(D,A,qc.getText().toUpperCase());
                if (dar.rechercheDepart("DAR"))
                {
                    dar.ajouter("DAR");
                    JOptionPane.showMessageDialog(null, "*****  Relation Ajouter  *****");
                    qc.setText("");
                }
                else JOptionPane.showMessageDialog(null, "Deja Existe");

            }   catch (UnknownHostException ex) {
                Logger.getLogger(AjoutDAR.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_ajouterActionPerformed

